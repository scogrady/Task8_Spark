package Twitter;

public class Coordinates {
	private String longitude;
	private String latitude;
	
	public String getLongitude() {
		return longitude;
	}
	
	public String getLatitude() {
		return latitude;
	}
	
	public void setLongitude (String s) {
		longitude = s;
	}
	
	public void setLatitude (String s) {
		latitude = s;
	}
	


}
