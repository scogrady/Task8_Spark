package Twitter;

public class Coordinates {
	private Double longitude;
	private Double latitude;
	
	public Double getLongitude() {
		return longitude;
	}
	
	public Double getLatitude() {
		return latitude;
	}
	
	public void setLongitude (Double s) {
		longitude = s;
	}
	
	public void setLatitude (Double s) {
		latitude = s;
	}
	


}
