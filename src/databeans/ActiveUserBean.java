package databeans;

public class ActiveUserBean {
	private String activeUser_name;
	private int activeUser_count;
	public String getActiveUser_name() {
		return activeUser_name;
	}
	public int getActiveUser_count() {
		return activeUser_count;
	}
	public void setActiveUser_name(String activeUser_name) {
		this.activeUser_name = activeUser_name;
	}
	public void setActiveUser_count(int activeUser_count) {
		this.activeUser_count = activeUser_count;
	}
}
